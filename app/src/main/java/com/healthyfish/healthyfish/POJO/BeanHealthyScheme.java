public class BeanHealthyScheme {
    // 健康计划背景
    private int healthySchemeBackground;
    // 健康计划名称
    private String healthySchemeTitle;
    // 健康计划内容
    private String healthySchemeContent;
    // 参与计划人数
    private String healthySchemeParticipant;

    public int getHealthySchemeBackground() {
        return healthySchemeBackground;
    }

    public void setHealthySchemeBackground(int healthySchemeBackground) {
        this.healthySchemeBackground = healthySchemeBackground;
    }

    public String getHealthySchemeTitle() {
        return healthySchemeTitle;
    }

    public void setHealthySchemeTitle(String healthySchemeTitle) {
        this.healthySchemeTitle = healthySchemeTitle;
    }

    public String getHealthySchemeContent() {
        return healthySchemeContent;
    }

    public void setHealthySchemeContent(String healthySchemeContent) {
        this.healthySchemeContent = healthySchemeContent;
    }

    public String getHealthySchemeParticipant() {
        return healthySchemeParticipant;
    }

    public void setHealthySchemeParticipant(String healthySchemeParticipant) {
        this.healthySchemeParticipant = healthySchemeParticipant;
    }
}
